void main() {
  //langkah 1
  int counter = 0; // Deklarasi dan inisialisasi variabel counter

  // Loop while untuk mencetak angka dari 0 hingga 32
  while (counter < 33) {
    print(counter);
    counter++;
  }

  // Loop do-while untuk mencetak angka dari 33 hingga 76
  do {
    print(counter);
    counter++;
  } while (counter < 77);
}
